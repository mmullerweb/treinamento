# Faça um programa que pergunte quanto você ganha por hora e o numero de horas trabalhadas no mes
# Calcule e mostre o total do seu salario.


valorhora = input("Digite Quanto voce ganha por hora?")
horastrab = input ("Digite Qual seu numero de horas trabalhadas?")

multiplicacao = int(valorhora) * int(horastrab) 

print("O total do salario é " + str (multiplicacao))

