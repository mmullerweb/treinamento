# Faça um programa que pergunte quanto você ganha por hora e o numero de horas trabalhadas no mes
# Calcule e mostre o total do seu salario.

valor = input ( "Quanto voce ganha por hora? ")
horas = input ( "Quantas horas voce trabalha no mes? ")

calculo = int (valor) * int (horas)

print (" Meu salario no mes e: " + str (calculo))
